
a = int(input("Faça o primeiro valor: "))
b = int(input("Faça o segundo valor: "))

soma = a + b
subtracao = a - b
multiplicacao = a * b
divicao = a / b
resto = a % b
print('soma: {soma} \nsubtração: {sub}'
        '\nMultiplicação: {mult} \nDivisão: {div} '
        '\nResto: {resto}'.format(soma=soma,
                                 sub=subtracao,
                                mult=multiplicacao,
                                div=divicao,
                                resto=resto))


